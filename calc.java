package com;
import java.util.*;

public class calculator {
   
    public int add(int first, int second) {

    	return  first + second;
            
    }
    public int sub(int first, int second) {

    	return  first - second;
            
    }
    public int mul(int first, int second) {

    	return  first * second;
            
    }
    public double div(double first, double second) {

    	return  first / second;
            
    }
   
}
